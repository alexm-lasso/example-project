package com.example.myprog.examples;

public class BaseMethods {

    private final int field1;
    private final String field2;

    public BaseMethods(int field1, String field2) {
        this.field1 = field1;
        this.field2 = field2;
    }

    public int getField1() {
        return field1;
    }

    public String getField2() {
        return field2;
    }

}
